#!/usr/bin/env node

import { copyFileSync, existsSync, readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { brotliCompressSync, gzipSync } from 'node:zlib';

const projectRoot = process.cwd();
const manifestPath = path.join(projectRoot, 'dist/.vite/manifest.json');
const reportPath = path.join(projectRoot, 'dist/bundle-stats.html');
const docPath = path.join(projectRoot, 'docs/performance/code-splitting.md');
const postArtifactPath = path.join(
  projectRoot,
  'docs/performance/artifacts/post-bundle-stats.html'
);

if (!existsSync(manifestPath)) {
  throw new Error(
    'Missing dist/.vite/manifest.json. Run ANALYZE=true vite build before updating metrics.'
  );
}

if (!existsSync(reportPath)) {
  throw new Error(
    'Missing dist/bundle-stats.html. Run ANALYZE=true vite build before updating metrics.'
  );
}

const manifest = JSON.parse(readFileSync(manifestPath, 'utf8'));
const manifestEntries = Object.entries(manifest);
const keyToEntry = new Map(manifestEntries);

const entryPair = manifestEntries.find(([, entry]) => entry.isEntry);
if (!entryPair) {
  throw new Error('No entry chunk found in manifest.');
}

const [entryKey, entryEntry] = entryPair;
const homeKey =
  (keyToEntry.has('src/app/pages/home.tsx') && 'src/app/pages/home.tsx') ||
  entryEntry.dynamicImports?.find(
    (importKey) => keyToEntry.get(importKey)?.src === 'src/app/pages/home.tsx'
  ) ||
  null;

const resolveClosure = (seedKeys) => {
  const visited = new Set();
  const queue = [...seedKeys];

  while (queue.length > 0) {
    const key = queue.shift();
    if (!key || visited.has(key)) {
      continue;
    }

    visited.add(key);
    const entry = keyToEntry.get(key);
    if (!entry) {
      continue;
    }

    for (const importedKey of entry.imports ?? []) {
      queue.push(importedKey);
    }
  }

  return visited;
};

const closureKeys = resolveClosure([entryKey, homeKey].filter(Boolean));

const allJsFiles = new Set(
  manifestEntries
    .map(([, entry]) => entry.file)
    .filter((file) => typeof file === 'string' && file.endsWith('.js'))
);

const initialJsFiles = new Set(
  [...closureKeys]
    .map((key) => keyToEntry.get(key)?.file)
    .filter((file) => typeof file === 'string' && file.endsWith('.js'))
);

const readMetric = (relativeFile) => {
  const absoluteFile = path.join(projectRoot, 'dist', relativeFile);
  const source = readFileSync(absoluteFile);

  return {
    raw: source.length,
    gzip: gzipSync(source).length,
    brotli: brotliCompressSync(source).length,
  };
};

const metricByFile = new Map();
for (const file of allJsFiles) {
  metricByFile.set(file, readMetric(file));
}

const sumMetric = (files, key) =>
  files.reduce(
    (total, file) => total + (metricByFile.get(file)?.[key] ?? 0),
    0
  );

const allJs = [...allJsFiles];
const initialJs = [...initialJsFiles];
const lazyJs = allJs.filter((file) => !initialJsFiles.has(file));

const entryFile = entryEntry.file;
const entryMetric = metricByFile.get(entryFile);
if (!entryMetric) {
  throw new Error(`Could not find entry metric for ${entryFile}.`);
}

const vendorFiles = allJs.filter((file) => /vendor/i.test(path.basename(file)));
const vendorRaw = vendorFiles.length > 0 ? sumMetric(vendorFiles, 'raw') : null;

const totalRaw = sumMetric(allJs, 'raw');
const totalGzip = sumMetric(allJs, 'gzip');
const totalBrotli = sumMetric(allJs, 'brotli');
const initialRaw = sumMetric(initialJs, 'raw');

const formatKb = (value) => (value / 1024).toFixed(2);
const today = new Intl.DateTimeFormat('en-US', {
  month: 'long',
  day: 'numeric',
  year: 'numeric',
}).format(new Date());

const autogeneratedSection = `<!-- AUTO-GENERATED:START -->
## Current post-change snapshot
Captured on **${today}** from \`dist/bundle-stats.html\`:

- Entry chunk (\`${entryFile}\`): \`${formatKb(entryMetric.raw)} KB raw\`, \`${formatKb(entryMetric.gzip)} KB gzip\`, \`${formatKb(entryMetric.brotli)} KB brotli\`
- Total initial JS for \`/\` (entry + home route dependency closure): \`${formatKb(initialRaw)} KB raw\`
- Total emitted JS across all chunks: \`${formatKb(totalRaw)} KB raw\`, \`${formatKb(totalGzip)} KB gzip\`, \`${formatKb(totalBrotli)} KB brotli\`
- Route-specific lazy chunks emitted: \`${lazyJs.length}\`

## Metrics table
| Metric | Baseline | Post-change | Delta |
|---|---:|---:|---:|
| Entry chunk (raw KB) |  | ${formatKb(entryMetric.raw)} |  |
| Entry chunk (gzip KB) |  | ${formatKb(entryMetric.gzip)} |  |
| Entry chunk (brotli KB) |  | ${formatKb(entryMetric.brotli)} |  |
| Total initial JS for \`/\` (raw KB) |  | ${formatKb(initialRaw)} |  |
| Vendor chunk (raw KB) |  | ${vendorRaw === null ? 'n/a' : formatKb(vendorRaw)} |  |
| Number of route-specific lazy chunks |  | ${lazyJs.length} |  |
<!-- AUTO-GENERATED:END -->`;

const docContent = readFileSync(docPath, 'utf8');
const autoSectionPattern =
  /<!-- AUTO-GENERATED:START -->[\s\S]*<!-- AUTO-GENERATED:END -->/;
if (!autoSectionPattern.test(docContent)) {
  throw new Error(
    'Could not find auto-generated section markers in docs/performance/code-splitting.md.'
  );
}

const updatedContent = docContent.replace(
  autoSectionPattern,
  autogeneratedSection
);

writeFileSync(docPath, updatedContent, 'utf8');
copyFileSync(reportPath, postArtifactPath);

console.log(
  'Updated code-splitting metrics document and post analyzer artifact.'
);
